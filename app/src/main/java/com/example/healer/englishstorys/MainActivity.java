    // deleted PlaceholderFragment
    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            // getItem is called to instantiate the fragment for the given page.
            // Return a PlaceholderFragment (defined as a static inner class below).
            switch (position) {
                case 0:
                    ReadStoryFraqment tab1 = new ReadStoryFraqment();
                    Intent intentService = new Intent(MainActivity.this,MyService.class);
                    intentService.putExtra("url",url);
                    intentService.putExtra("position",position);
                    startService(intentService);
                    return tab1;
                case 1:
                    TranslateFragment tab2 = new TranslateFragment();
                    return tab2;
                case 2:
                    Grammar_VocabularyFragment tab3 = new Grammar_VocabularyFragment();
                    return tab3;
                case 3:
                    CheckByQuestionFragment tab4 = new CheckByQuestionFragment();
                    return tab4;
                default:
                    return null;
            }
        }

        @Override
        public int getCount() {
            // Show 4 total pages.
            return 4;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "Read Story";
                case 1:
                    return "Translate";
                case 2:
                    return "Grammar and Vocabulary";
                case 3:
                    return "Check by Question";
            }
            return null;
        }
    }
